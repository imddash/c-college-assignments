// Declare int, char, float and double variable, store different values and display them

#include <stdio.h>

void main()
{
    int a = 10;
    char b = 'a';
    float c = 10.5;
    double d = 10.5;
    printf("a = %d\n", a);
    printf("b = %c\n", b);
    printf("c = %f\n", c);
    printf("d = %lf\n", d);
}